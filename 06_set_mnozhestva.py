print("+++++++++++ ЗАДАЧА НА МНОЖЕСТВА  ++++++++++++")
# супруги собираются в отпуск, каждый из них взял какие-то вещи
# Максим взял
max_took = {"телефон", "бритва", "рубашка", "шорты"}
# Катя взяла
katya_took = {"телефон", "шорты", "зонтик", "помада"}
# Какие вещи взяли супруги
total_took = max_took | katya_took
print(total_took)
# Какие вещи повторятются
print(max_took & katya_took)
# Какие вещи взял Максим и не взяла Катя
print(max_took - katya_took)
# Какие вещи взяла Катя и не взял Максим
print(katya_took - max_took)






