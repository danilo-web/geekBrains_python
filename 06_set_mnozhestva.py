print(cities)
cars = {"honda", "mazda", "BMW"}  # множество обьявляется в фигурных скобках
cars.add("lada")  # добавление элемента
cars.remove("BMW")  # удаление, если элемента нет то выдаст ошибку
cars.discard("BMW")  # удаление, если элемента нет то никакой ошибки не выдаст
print(cars, "порядок вывода может каждый раз меняться если множество большое!")
print(sorted(cars))
print("BMW" in cars)  # вернет тру или фолс
